package CoreJava.Operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        int a=30,b=10,c=0;
        c=a+b;
        System.out.println(c);
        c=a-b;
        System.out.println(c);
        c=a+b;
        System.out.println(c);
        c+=a;
        System.out.println(c);
        c-=a;
        System.out.println(c);
        c*=a;
        System.out.println(c);
        c/=a;
        System.out.println(c);
    }
}
